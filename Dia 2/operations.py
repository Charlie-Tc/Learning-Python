# Operadores matemáticos

# "+" = suma
# "-" = resta
# "*" = multiplicación
# "/" = división
# "//" = división al piso
# "%" = módulo
# "**" = potencia
# "**0.5" = raíz cuadrada

x = 6
y = 2
z = 7

print(f"{x} mas {y} es igual a {x + y}")
print(f"{x} menos {y} es igual a {x - y}")
print(f"{x} por {y} es igual a {x * y}")
print(f"{x} dividido {y} es igual a {x / y}")

print(f"{x} dividido al piso {y} es igual a {x // y}")
print(f"{x} módulo de {y} es igual a {x % y}")
print(f"{x} elevado a la {y} es igual a {x ** y}")
print(f"{x} elevado a la {3} es igual a {x ** 3}")
print(f"la raíz cuadrada de {y} es igual a {x ** 0.5}")